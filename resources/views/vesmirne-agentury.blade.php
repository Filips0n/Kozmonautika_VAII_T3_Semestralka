            <a class="anchor" id="NASA"></a>

            <img class="agency-logo" src="../assets/nasa-logo-web-rgb.png" alt="Nasa logo">
            <p class="agency-text">
                <strong>Národný úrad pre letectvo a vesmír</strong> je nezávislá agentúra federálnej vlády USA
                zodpovedná za civilný vesmírny program, ako aj za letecký a vesmírny výskum.

                NASA bola založená v roku 1958 ako nástupca Národného poradného výboru pre letectvo (NACA). Nová
                agentúra mala mať výrazne civilnú orientáciu a mala podporovať mierové aplikácie vo vesmírnej vede. Od
                svojho založenia väčšinu úsilia o prieskum vesmíru v USA viedla NASA, vrátane pristávacích misií Apollo
                Moon, vesmírnej stanice Skylab a neskôr raketoplánu. NASA podporuje Medzinárodnú vesmírnu stanicu a
                dohliada na vývoj kozmickej lode Orion, systému Space Launch System, vozidiel Commercial Crew a
                plánovanej vesmírnej stanice Lunar Gateway. Agentúra je tiež zodpovedná za program Launch Services,
                ktorý poskytuje dohľad nad operáciami štartu a správu odpočítavania pri štartoch bez posádky NASA.

                Veda NASA sa zameriava na lepšie porozumenie Zeme prostredníctvom systému pozorovania Zeme, pokrok v
                heliofyzike prostredníctvom úsilia výskumného programu Heliofyziky riaditeľstva vedeckej misie skúmanie
                telies v celej slnečnej sústave pomocou pokročilých robotických kozmických lodí, ako napríklad New
                Horizons a skúmanie astrofyzikových tém, ako je napríklad Veľký tresk, prostredníctvom Veľkých
                observatórií a súvisiacich programov.
            </p>

            <a class="anchor" id="Roscosmos"></a>

            <img class="agency-logo" src="../assets/Roscosmos.png" alt="Roscosmos logo">
            <p class="agency-text">
                <strong>Roscosmos</strong> je štátna spoločnosť Ruskej federácie zodpovedná za vesmírne lety,
                kozmonautické programy a letecký výskum.

                Roskosmos, ktorý pochádza zo sovietskeho vesmírneho programu založeného v päťdesiatych rokoch minulého
                storočia, vznikol po rozpade Sovietskeho zväzu v roku 1991. Pôvodne začínal ako Ruská vesmírna agentúra,
                ktorá bola založená 25. februára 1992 a reštrukturalizovaná v roku 1999. a 2004, ako Ruská letecká a
                vesmírna agentúra a Federálna vesmírna agentúra (Roscosmos). V roku 2015 bola Federálna vesmírna
                agentúra (Roscosmos) zlúčená so Spojenou raketou a vesmírom korporáciou, s cieľom znárodniť ruský
                vesmírny priemysel, čo vedie k súčasnému stavu Roskosmosu.

                Roscosmos má ústredie v Moskve, hlavné centrum riadenia misií v neďalekom meste Koroljov a výcvikové
                stredisko kozmonautov Jurija Gagarina sa nachádza v meste Star City v Moskovskej oblasti. Medzi jeho
                štartovacie zariadenia patrí kozmodróm Bajkonur v Kazachstane, prvý a najväčší vesmírny prístav na
                svete, a kozmodróm Vostočnyj, ktorý sa buduje na ruskom Ďalekom východe v Amurskej oblasti. Jeho
                riaditeľom je od mája 2018 Dmitrij Rogozin.

                Ako hlavný nástupca sovietskeho vesmírneho programu zahŕňa dedičstvo Roscosmosu prvý satelit na svete,
                prvý vesmírny let s ľuďmi a prvú vesmírnu stanicu. Medzi jej súčasné aktivity patrí Medzinárodná
                vesmírna stanica, kde je hlavným partnerom. 22. februára 2019 oznámil Roscosmos výstavbu svojho nového
                sídla v Moskve, Národného vesmírneho centra. Je to astronautský zbor, ktorý je prvým vo svetovej
                histórii.
            </p>

            <a class="anchor" id="ESA"></a>

            <img class="agency-logo" src="../assets/ESA_logo.png" alt="ESA logo">
            <p class="agency-text">
                <strong>Európska vesmírna agentúra</strong> (ESA, European Space Agency) je medzivládna organizácia na
                výskum vesmíru založená v roku 1974, ktorá má v súčasnosti 22 členských štátov. Sídlo jej riaditeľstva
                je v Paríži a jej jednotlivé centrá sú vo zvyšných členských štátoch. Zamestnáva zhruba 1 900
                zamestnancov (vrátane subdodávateľov a zamestnancov jednotlivých štátnych kozmických agentúr). Na rok
                2013 mala plánovaný ročný rozpočet 4,282 miliárd €.

                Kozmodróm ESA je Guyanské kozmické centrum v Kourou v juhoamerickej Francúzskej Guyane, z ktorého je
                možné vďaka jeho blízkosti k rovníku ľahko dosiahnuť komerčne dôležité obežné dráhy. Vďaka spoľahlivému
                a výkonnému nosiču Ariane 4 ESA získala dôležité miesto na trhu kozmických dopravcov v súčasnosti
                posilnené novou výkonnou raketou Ariane 5. Zároveň sa v posledných rokoch stala druhým najvýznamnejším
                hráčom na poli kozmického výskumu spolu s NASA.
            </p>

            <a class="anchor" id="CNSA"></a>

            <img class="agency-logo" src="../assets/China_National_Space_Administration_logo.png"
                 alt="China_National_Space_Administration logo">
            <p class="agency-text">
                <strong>Kuo-ťia chang-tchien ťü</strong> je čínska civilná organizácia, ktorá je zodpovedná za
                smerovanie čínskeho kozmického programu.
                CNSA vznikla na základe predchádzajúceho kozmického (a hlavne raketového) programu, ktorý sa začal na
                konci 60. rokov snahou Číny o vlastný raketový nosič jadrových hlavíc. Prvá družica Tung-fang-chung 1
                bola vyslaná do vesmíru v roku 1970. Vážila 173 kg a na palube mala rádiový vysielač, ktorý vysielal
                budovateľskú pieseň Východ je červený. V ďalších rokoch Čína vyslala niekoľko telekomunikačných
                satelitov, meteorologických družíc a samozrejme aj špionážne satelity.

                Najväčší úspech dosiahla Čína v roku 2003, kedy došlo k vypusteniu kozmickej lode Šen-čou (Božská loď) s
                ľudskou posádkou. K vypusteniu lode bola použitá nosná raketa Čchang-čeng 2F (Dlhý pochod 2F). V
                septembri 2011 sa Číne podarilo vyslať na obežnú dráhu Zeme vlastnú kozmickú stanicu Tchien-kung 1
                (Nebeský palác 1) ako odrazový mostík pre ďalšie cesty na Mesiac a Mars. V septembri 2016 bola úspešne
                vynesená na obežnú dráhu druhá čínska kozmická stanica Tchien-kung 2.
            </p>

            <a class="anchor" id="JAXA"></a>

            <img class="agency-logo" src="../assets/Jaxa.png" alt="Jaxa logo">
            <p class="agency-text">
                1. októbra 2003 sa zlúčili tri organizácie a vytvorili novú <strong>JAXA</strong>: Japonský inštitút pre
                vesmír a astronautiku (ISAS), Japonské národné vesmírne laboratórium (NAL) a Japonská národná agentúra
                pre rozvoj vesmíru (NASDA). JAXA bola vytvorená ako nezávislá administratívna inštitúcia spravovaná
                ministerstvom školstva, kultúry, športu, vedy a technológie (MEXT) a ministerstvom vnútra a komunikácií
                (MIC).
                Pred zlúčením bol ISAS zodpovedný za vesmírny a planetárny výskum, zatiaľ čo NAL sa zameriaval na
                letecký výskum. NASDA, ktorá bola založená 1. októbra 1969, vyvinula rakety, satelity a tiež postavila
                japonský experimentálny modul. Staré sídlo NASDA sa nachádzalo na súčasnom mieste vesmírneho centra
                Tanegashima na ostrove Tanegashima, 115 kilometrov južne od Kyushu. NASDA školil aj japonských
                astronautov, ktorí lietali s americkými raketoplánmi.

                Základný vesmírny zákon bol schválený v roku 2008 a jurisdikčná autorita JAXA sa presťahovala z MEXT do
                strategického veliteľstva pre rozvoj vesmíru (SHSD) v kabinete vedeného predsedom vlády. V roku 2016 bol
                kabinet zriadený národným sekretariátom pre vesmírnu politiku (NSPS).
                Plánovanie medziplanetárnych výskumných misií, ako napríklad ASTRO-E, môže trvať až sedem rokov.
                Vzhľadom na časový posun medzi týmito medziplanetárnymi udalosťami a časom plánovania misie môžu byť
                príležitosti na získanie nových znalostí o vesmíre stratené. Aby sa tomu zabránilo, plánuje JAXA od roku
                2010 používať menšie a rýchlejšie misie.

                V roku 2012 nová legislatíva rozšírila pôsobnosť JAXA z mierových účelov len na určitý rozvoj vojenského
                priestoru, ako sú systémy včasného varovania pred raketami. Politická kontrola nad JAXA prešla z MEXTU
                na kabinet predsedu vlády prostredníctvom nového úradu pre vesmírnu stratégiu.
            </p>

            <a class="anchor" id="CSA"></a>

            <img class="agency-logo" src="../assets/canadian-space-agency.png" alt="CSA logo">
            <p class="agency-text">
                <strong>Kanadská vesmírna agentúra</strong> je kanadská vládna kozmická agentúra, ktorá zabezpečuje
                vesmírny program tejto krajiny. Bola založená v marci 1989. Na jej čele stojí prezident, ktorý sa
                zodpovedá ministrovi priemyslu. Sídli v meste Quebec, ale pobočky má tiež na iných miestach: v Ottawe,
                Washingtone, Paríži, na Myse Canaveral a v Houstone. Má cca 575 zamestnancov.
                Kanada sa zapája do celosvetového kozmického výskumu v oblasti technológií, expertíz a personálu, najmä
                v spolupráci s NASA a ESA.

                Kanada má niekoľkých astronautov, výrazný je tiež kanadský technologický prínos na stavbe a prevádzke
                Medzinárodnej vesmírnej stanice. Ide o robotický systém Canadarm, ktorý je súčasťou raketoplánu a
                Canadarm2 a mobilná platforma Mobile Servicing System na Medzinárodnej vesmírnej stanici (ISS). Pre
                lepšiu orientáciu okolo raketoplánu a kozmickej stanice využívajú Canadarm a Canadarm2 program Advanced
                Space Vision System, ktorý presne určí požadovanú pozíciu v 3D priestore. Ďalším kanadským príspevkom je
                Orbiter Boom Sensor System, ktorý sa využíva pri kontrole poškodenia raketoplánu na obežnej dráhe.
            </p>

            <a class="anchor" id="ISRO"></a>

            <img class="agency-logo" src="../assets/Indian_Space_Research_Organisation_Logo.svg.png"
                 alt="Indian_Space_Research_Organisation logo">
            <p class="agency-text">
                <strong>Indická organizácia pre výskum vesmíru</strong> (ISRO) je národná vesmírna agentúra v Indii so
                sídlom v Bengaluru. Pôsobí pod ministerstvom vesmíru (DOS), na ktoré priamo dohliada predseda vlády
                Indie, pričom predseda ISRO je zároveň výkonným riaditeľom DOS. ISRO je primárnou agentúrou v Indii,
                ktorá vykonáva úlohy súvisiace s vesmírnymi aplikáciami, prieskumom vesmíru a vývojom súvisiacich
                technológií. Je to jedna zo šiestich vládnych vesmírnych agentúr na svete, ktoré disponujú úplnými
                štartovacími schopnosťami, rozmiestňujú kryogénne motory, štartujú mimozemské misie a prevádzkujú veľké
                flotily umelých satelitov.

                Indický národný výbor pre vesmírny výskum (INCOSPAR) zriadil Jawaharlal Nehru v rámci ministerstva pre
                atómovú energiu (DAE) v roku 1962 na naliehanie vedca Vikrama Sarabhaia, ktorý uznáva potrebu vesmírneho
                výskumu. INCOSPAR rástol a stal sa ISRO v roku 1969 v rámci DAE. V roku 1972 indická vláda zriadila
                vesmírnu komisiu a ministerstvo vesmíru (DOS), čím sa ISRO dostalo pod DOS. Zriadenie ISRO teda v Indii
                inštitucionalizovalo aktivity v oblasti vesmírneho výskumu. Odvtedy ho spravuje DOS, ktorý riadi rôzne
                ďalšie inštitúcie v Indii v oblasti astronómie a vesmírnych technológií.

                ISRO zostrojil prvý indický satelit Aryabhata, ktorý vypustil Sovietsky zväz 19. apríla 1975. V roku
                1980 ISRO vypustil satelit RS-1 na palubu vlastného SLV-3, čím sa India stala šiestou krajinou, ktorá je
                schopná podnikať na obežnú dráhu. Po SLV-3 nasledoval ASLV, po ktorom nasledoval vývoj mnohých stredných
                leteckých nosných rakiet, raketových motorov, satelitných systémov a sietí, ktoré agentúre umožnili
                vypustiť stovky domácich a zahraničných satelitov a rôzne misie do hlbokého vesmíru na prieskum vesmíru.
            </p>

